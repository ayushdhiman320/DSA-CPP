#include <bits/stdc++.h>
using namespace std;
int main(){

    // For loop
    for(int i=1; i<=10;i++)
    {
        cout<<"Ayush"<<endl;
    }
    
    for(int i=5;i>=1;i--)
    {
        cout<<"hello "<<i<<endl;
    }



    //while loop
    int i=1;
    while(i<=5)
    {
        cout<<"How are you..! "<<i<<endl;
        i++;
    }


    //do while loop
    int j=2;
    do{
        cout<<"hello from ayush"<<endl;
        j++;
    }while(j<=1);

    return 0;
}